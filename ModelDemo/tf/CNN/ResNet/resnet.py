@tf.function
def block(X: tf.Tensor,
          kernel_size: int,
          filters: typing.List[int],
          stage_no: int,
          block_name: str,
          is_conv_layer: bool = False,
          stride: int = 2,
          ) -> tf.Tensor:
    # names
    conv_name_base = 'res' + str(stage_no) + block_name + '_branch'
    bn_name_base = 'bn' + str(stage_no) + block_name + "_branch"
    # filters
    F1, F2, F3 = filters
    # save the input value for shortcut
    x_shortcut = X

    # first component
    x = Conv2D(filters=F1,
               kernel_size=(1, 1),
               strides=(stride, stride) if is_conv_layer else (1, 1),
               padding='valid',
               name=conv_name_base + "2a",
               kernel_initializer="glorot_uniform")(X)

    x = BatchNormalization(axis=3, name=bn_name_base + "2a")(x)
    x = Activation(activation='relu')(x)

    # second component
    x = Conv2D(filters=F2,
               kernel_size=(kernel_size, kernel_size),
               strides=(1, 1),
               padding='same',
               name=conv_name_base + "2b",
               kernel_initializer="glorot_uniform"
               )(x)
    x = BatchNormalization(axis=3, name=bn_name_base + "2b")(x)
    x = Activation(activation='relu')(x)

    # third component
    x = Conv2D(filters=F3,
               kernel_size=(1, 1),
               strides=(1, 1),
               padding='valid',
               name=conv_name_base + "2c",
               kernel_initializer="glorot_uniform")(x)
    x = BatchNormalization(axis=3, name=bn_name_base + "2c")(x)
    # 因为is_conv_layer=True, first component的strides不一定等于1，所以在跳连的时候，x_shortcut要按照F3的filters，F1的strides
    # 将输入进行转换才能和F3输出的dimension相匹配
    if is_conv_layer:
        x_shortcut = Conv2D(filters=F3,
                            kernel_size=(1, 1),
                            strides=(stride, stride),
                            padding='valid',
                            name=conv_name_base + "1",
                            kernel_initializer="glorot_uniform")(x_shortcut)
        x_shortcut = BatchNormalization(axis=3, name=bn_name_base + "1")(x_shortcut)
    outputs = Add()([x, x_shortcut])
    outputs = Activation(activation='relu')(outputs)
    return outputs


@tf.function
def resnet(name: str,
           layers: typing.List[int],
           input_shape: typing.Tuple[int] = (64, 64, 3),
           classes: int = 6) -> Model:
    """
        Implementation of the popular ResNet architecture.
        Arguments:
        name        -- name of the architecture
        layers      -- number of blocks per layer
        input_shape -- shape of the images of the dataset
        classes     -- integer, number of classes
        Returns:
        model       -- a Model() instance in Keras
        Model Architecture:
        Resnet50:
            CONV2D -> BATCHNORM -> RELU -> MAXPOOL  // conv1
                -> CONVBLOCK -> IDBLOCK * 2         // conv2_x
                -> CONVBLOCK -> IDBLOCK * 3         // conv3_x
                -> CONVBLOCK -> IDBLOCK * 5         // conv4_x
                -> CONVBLOCK -> IDBLOCK * 2         // conv5_x
                -> AVGPOOL
                -> TOPLAYER
        Resnet101:
            CONV2D -> BATCHNORM -> RELU -> MAXPOOL  // conv1
                -> CONVBLOCK -> IDBLOCK * 2         // conv2_x
                -> CONVBLOCK -> IDBLOCK * 3         // conv3_x
                -> CONVBLOCK -> IDBLOCK * 22        // conv4_x
                -> CONVBLOCK -> IDBLOCK * 2         // conv5_x
                -> AVGPOOL
                -> TOPLAYER
        Resnet152:
            CONV2D -> BATCHNORM -> RELU -> MAXPOOL  // conv1
                -> CONVBLOCK -> IDBLOCK * 2         // conv2_x
                -> CONVBLOCK -> IDBLOCK * 7         // conv3_x
                -> CONVBLOCK -> IDBLOCK * 35        // conv4_x
                -> CONVBLOCK -> IDBLOCK * 2         // conv5_x
                -> AVGPOOL
                -> TOPLAYER
        """
    # get layers (layer1 is always the same so no need to provide)
    layer2, layer3, layer4, layer5 = layers
    # convert input shape into tensor
    X_input = Input(input_shape)
    # zero-padding
    X = ZeroPadding2D(padding=(3, 3))(X_input)

    # conv1
    X = Conv2D(
        filters=64,
        kernel_size=(7, 7),
        strides=(2, 2),
        name='conv1',
        kernel_initializer="glorot_uniform"
    )(X)

    X = BatchNormalization(axis=3, name='bn_conv1')(X)
    X = Activation(activation='relu')(X)
    X = MaxPooling2D(pool_size=(3, 3), strides=(2, 2))(X)

    # conv2_x
    X = make_layer(X=X, layers=layer2, kernel_size=3, filters=[64, 64, 256], stride=1, stage_no=2)
    # conv3_x
    X = make_layer(X=X, layers=layer3, kernel_size=3, filters=[128, 128, 512], stride=2, stage_no=3)
    # conv4_x
    X = make_layer(X=X, layers=layer4, kernel_size=3, filters=[256, 256, 1024], stride=2, stage_no=4)
    # conv5_x
    X = make_layer(X=X, layers=layer5, kernel_size=3, filters=[512, 512, 2048], stride=1, stage_no=5)

    # average pooling
    X = AveragePooling2D(pool_size=(2, 2), name='avg_pool')(X)

    # output layer
    X = Flatten()(X)
    X = Dense(units=classes, activation='softmax', name='fc' + str(classes), kernel_initializer="glorot_uniform")(X)
    model = Model(inputs=X_input, outputs=X, name=name)
    return model


def make_layer(X: tf.Tensor,
               layers: int,
               kernel_size: int,
               filters: typing.List[int],
               stride: int,
               stage_no: int) -> tf.Tensor:
    # create convolution block
    X = block(X=X,
              kernel_size=kernel_size,
              filters=filters,
              stride=stride,
              stage_no=stage_no,
              block_name='a',
              is_conv_layer=True,
              )

    # create identity block
    block_name_ordinal = ord("b")

    for _ in range(layers - 1):
        X = block(
            X=X,
            kernel_size=kernel_size,
            filters=filters,
            stage_no=stage_no,
            block_name=chr(block_name_ordinal)
        )
        block_name_ordinal += 1

    return X


